    const spaceAbove = a.top - GAP - MARGIN;
    const placeBelow = spaceBelow >= h || spaceBelow >= spaceAbove;
    let top = placeBelow ? a.bottom + GAP : a.top - GAP - h;
    top = Math.max(MARGIN, Math.min(top, vh - MARGIN - h));
    let left = a.left;
